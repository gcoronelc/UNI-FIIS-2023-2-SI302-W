package pe.edu.uni.aprendiendojava.service;

public class MateService {
	
	public int sumar(int n1, int n2){
		System.out.println("Suma de enteros");
		return (n1+n2);
	}
	
	public float sumar(float n1, float n2){
		System.out.println("Suma de float");
		return (n1+n2);
	}
	
	public double sumar(double n1, double n2){
		System.out.println("Suma de double");
		return (n1+n2);
	}
	
	public double sumar(long n1, float n2){
		System.out.println("Suma de long y float");
		return (n1+n2);
	}
	
}
