   public void setAccion(String accion) {
      this.accion = accion;
      model = new ClienteModel();
      if (Session.get("clienteModel") != null) {
         model = (ClienteModel) Session.get("clienteModel");
      }
      Session.put("clienteModel", null); // Fundamental
      String titulo = accion + " CLIENTE";
      this.setTitle(titulo);
      // Datos del cliente
      txtId.setText(model.getId() + "");
      txtNombre.setText(model.getNombre());
      txtApellido.setText(model.getApellido());
      txtDNI.setText(model.getDni());
      txtDistrito.setText(model.getDistrito());
      txtCorreo.setText(model.getCorreo());
      txtTelefono.setText(model.getTelefono());
      // Activar controles
      txtNombre.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
      txtApellido.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
      txtDNI.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
      txtDistrito.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
      txtCorreo.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
      txtTelefono.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
   }

