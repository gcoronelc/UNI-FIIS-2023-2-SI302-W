   private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
      // Recoger los datos
      model.setNombre(txtNombre.getText());
      model.setApellido(txtApellido.getText());
      model.setDni(txtDNI.getText());
      model.setDistrito(txtDistrito.getText());
      model.setCorreo(txtCorreo.getText());
      model.setTelefono(txtTelefono.getText());
      CrudClienteController controller = new CrudClienteController();
      switch (accion) {
         case UtilView.CRUD_NUEVO:
            controller.insert(model);
            break;
         case UtilView.CRUD_EDITAR:
            controller.update(model);
            break;
         case UtilView.CRUD_ELIMINAR:
            controller.delete(model.getId());
            break;
      }
      Session.put("aceptar", "1"); // 1 se ejecuto la accion correctamente
      Session.put("clienteModel", model);
      this.dispose();
   }//GEN-LAST:event_btnAceptarActionPerformed

