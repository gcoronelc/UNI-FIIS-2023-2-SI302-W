	private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
            VentaItem item = new VentaItem();
            int fila = tblData.getSelectedRow();
            Producto prod = productos.get(fila);
            item.setIdprod(prod.getIdprod());
            item.setNombre(prod.getNombre());
            item.setPrecio(prod.getPrecio());
            item.setCant(Integer.parseInt(txtCant.getText()));
            item.setSubtotal(item.getPrecio() * item.getCant());
            Session.put("ventaItem", item);
            this.dispose();
	}//GEN-LAST:event_btnAceptarActionPerformed

