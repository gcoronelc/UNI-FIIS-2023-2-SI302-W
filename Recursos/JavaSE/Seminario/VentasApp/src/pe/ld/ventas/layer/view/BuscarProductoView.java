    private void limpiarTabla() {
        DefaultTableModel dtm = (DefaultTableModel) tblData.getModel();
        dtm.setRowCount(0);
    }

    private void mostrar(List<Producto> productos) {
        DefaultTableModel dtm = (DefaultTableModel) tblData.getModel();
        for (Producto p : productos) {
            Object[] rowData = {p.getIdprod(), p.getNombre(), p.getPrecio(), p.getStock()
            };
            dtm.addRow(rowData);
        }
        tblData.setRowSelectionInterval(0, 0);
    }
